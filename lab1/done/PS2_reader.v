// ?????? ??? ?????? ?????? ? PS/2 ??????????
module PS2_reader(
    input clk,            // ????????? ???????? ??????
    input PS2_DATA,       // ????? ?????? PS/2
    input PS2_CLK,        // ????? ????????? ??????? PS/2
    output reg ERROR,     // ???? ??????
    output reg R_O,       // ???? ?????????? ??????
    output [7:0] code     // 8-?????? ???????? ???
);

    // ???????? ????????????? ??? ????? ?????? ? ????????? ??????? PS/2
    reg [1:0] PS2_data_sync;
    wire ps2_data;
    assign ps2_data = PS2_data_sync[1];  // ?????????????????? ?????? PS/2

    reg [1:0] PS2_clk_sync;
    wire ps2_clk;
    assign ps2_clk = PS2_clk_sync[1];    // ?????????????????? ???????? ?????? PS/2

    // ??????? ????????????? ? ????????? ???????? ????????
    always @(posedge clk) begin
        PS2_data_sync <= {PS2_data_sync[0], PS2_DATA};
        PS2_clk_sync <= {PS2_clk_sync[0], PS2_CLK};
    end

    // ????????? ???????? ?????? ?????? ? ?????????? ??????
    initial begin
        ERROR <= 0;
        R_O <= 0;
        PS2_data_sync <= 2'b11;
        PS2_clk_sync <= 2'b11;
    end

    // ??????????? ?????????
    localparam [3:0] 
        START = 0,      // ????????? ?????????? ????
        LOAD = 1,       // ????????? ???????? ????? ??????
        END = 2,        // ???????? ????????? (????-???)
        PARITY_BIT = 3, // ????????? ???????? ???? ????????
        WAIT = 5;       // ????????? ??????/????????

    reg [3:0] state;            // ?????????? ??? ???????? ???????? ?????????
    reg [3:0] bits_counter;     // ??????? ????? PS/2

    // ?????????? ???????? ?? ????????? ?????? ????????? ??????? PS/2
    always @(negedge ps2_clk) 
        bits_counter <= (bits_counter != 4'd10) ? (bits_counter + 1) : 0;

    // ????????? ???????? ????????? ? ???????? ?????
    initial begin
        state = START;
        bits_counter = 0;
    end

    // ????? ??? ???????? ???????? ?????? PS/2
    reg [7:0] ps2_buff;
    initial ps2_buff = 0;
    assign code = ps2_buff;

    // ?????? ????????? ??? ?????? ?????? PS/2
    always @(negedge ps2_clk) begin
        case(state)
            // ????????? START: ???????? ?????????? ???? (?????????? 0)
            START: begin
                ps2_buff <= 0;          // ????? ??????
                R_O <= 0;               // ????? ????? ?????????? ??????
                ERROR <= 0;             // ????? ????? ??????
                state <= ~ps2_data ? LOAD : WAIT;  // ??????? ??? ???????? ????????? ????
            end
            
            // ????????? LOAD: ????? ????? ??????
            LOAD: begin
                if(bits_counter == 8)   // ???? ????????? 8 ????? ??????
                    state <= PARITY_BIT;
                ps2_buff <= {ps2_data, ps2_buff[7:1]};  // ????? ??????
            end
            
            // ????????? PARITY_BIT: ???????? ???? ????????
            PARITY_BIT:
                state <= (~^(ps2_buff) == ps2_data) ? END : WAIT;

            // ????????? END: ???????? ????-???? ? ????????? ???????? ??????
            END: begin
                state <= START;         // ??????? ? ????????? START
                ERROR <= ps2_data ? 0 : 1;  // ???????? ????-???? (?????????? 1)
                R_O <= 1;               // ????????? ????? ?????????? ??????
            end
            
            // ????????? WAIT: ????????? ??????
            WAIT: if(bits_counter == 10) begin
                state <= START;         // ??????? ? ????????? START
                R_O <= 1;               // ????????? ????? ?????????? ??????
                ERROR <= 1;             // ????????? ????? ??????
            end
        endcase 
    end

endmodule
