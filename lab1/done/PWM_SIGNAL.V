module PWM_SIGNAL #(
    parameter PWM_out = 1024,                   // ?????????? ??? ???????
    localparam BIT_COUNT = $clog2(PWM_out)      // ?????????? ??? ??? ????????
)

(
    input clk,                                 // ??????? ????????? ???????? ??????
    input rst,                                 // ?????? ??????
    input ce,                                  // ?????? ??????????
    input [BIT_COUNT - 1:0] PWM_in,             // ??????? ???????? ??? ???????
    output reg PWM_signal                       // ???????? ??? ??????
);

    // ???????? ??? ?????????? ???????? ???????? ? ???????? ???????? ?????????
    reg [BIT_COUNT - 1:0] PWM_in_reg, clk_counter;

    // ????????????? ????????? ????????
    initial begin
        PWM_signal = 1'b0;
        PWM_in_reg = {BIT_COUNT{1'b0}};
        clk_counter = {BIT_COUNT{1'b0}};
    end

    // ???????? ??????? ??? ????????? ??? ???????
    always @(posedge clk) begin
        if (rst) begin
            // ????? ????????? ??? ???????? ??????? ??????
            PWM_in_reg = {BIT_COUNT{1'b0}};
            clk_counter = {BIT_COUNT{1'b0}};
        end
        else if (ce) begin
            // ?????????? ???????? ???????? ??? ?????? ????? ???????
            if (clk_counter == 0)
                PWM_in_reg <= PWM_in;
            
            // ????????? ???????? ??? ????? ??? ?????????? ????????????? ????????
            clk_counter <= clk_counter == PWM_out - 1 ? 0 : clk_counter + 1;

            // ????????? ??? ??????? ? 1, ???? ??????? ????? 1
            if (clk_counter == 1)
                PWM_signal <= 1'b1;

            // ????????? ??? ??????? ? 0, ???? ??????? ????? `PWM_in_reg + 1`
            if (clk_counter == PWM_in_reg + 1)
                PWM_signal <= 1'b0;
        end
    end

endmodule
