`define HIGH_SPEED 2 // 2 ??????? ?????? ????????
`define MIDDLE_SPEED 6 // 6 ?????? ?????? ????????
`define LOW_SPEED 10 // 10 ?????? ?????? ????????
`define MAX_SPEED 10

// ?????? ??????????? ??????????? ? ?????????? ?????????
module LED_controller #(
    parameter CLOCK_RATE = 100_000_000,            // ??????? ?????????? ????????? ???????
    localparam COUNT_CHANGES = 512,                // ?????????? ????????? ?????
    localparam MAX_COUNTER = CLOCK_RATE / COUNT_CHANGES // ???????????? ???????? ????????
)

(
    input clk,           // ??????? ????????? ???????? ??????
    input up_speed,      // ?????? ??? ?????????? ???????? ????? ?????
    input low_speed,     // ?????? ??? ?????????? ???????? ????? ?????
    output [7:0] light   // ???????? ?????? ??????? ??????????
);

    // ??????????? ?????????? ??? ?????????? ?????????
    reg [$clog2(`MAX_SPEED) - 1:0] speed;
    reg [$clog2(`MAX_SPEED * MAX_COUNTER) - 1:0] clk_counter;

    // ????????? ???????? ??? CORDIC
    reg [15:0] Xin, Yin;
    reg [63:0] i = 0;
    wire [31:0] cordic_angle = (((1 << 32) * i) >> 9);

    // ????????? ???????? ??? CORDIC
    localparam start_X = 4000;

    // ????????? ??????? CORDIC (???????)
    wire signed [16:0] cos_out;

    // ????????? ?????? CORDIC ??? ?????????? ???????????
    CORDIC cordic_light (
        .clk(clk),
        .angle(cordic_angle),
        .x_in(Xin),
        .y_in(Yin),
        .cos_out(cos_out)
    );

    // ?????????????? ????????? ???????? CORDIC ? ?????? ??????? ??????????
    assign light = ((cos_out + $signed(start_X + 8)) >>> 6);

    // ????????? ?????????????
    initial begin
        Xin = start_X / 1.647;
        Yin = 0;
        
        speed = `MIDDLE_SPEED;
        clk_counter = {$clog2(`MAX_SPEED * MAX_COUNTER){1'b0}};
    end

    // ??????, ??????????? ?? ????????? ????
    wire angle_changed = clk_counter == speed * MAX_COUNTER - 1;

    // ??????? ???????? ?????????
    always @(posedge clk)
        clk_counter = angle_changed ? {$clog2(`MAX_SPEED * MAX_COUNTER){1'b0}} : clk_counter + 1;

    // ????????? ???? ?? ?????? ????????
    always @(posedge clk)
        if (angle_changed) begin
            i = (i == COUNT_CHANGES - 1) ? 0 : i + 1;
        end

    // ?????????? ????????? ????????? ???????
    always @(posedge clk) begin
        if (low_speed)
            case (speed)
                `HIGH_SPEED: speed <= `MIDDLE_SPEED;
                `MIDDLE_SPEED: speed <= `LOW_SPEED;
            endcase
        
        if (up_speed)
            case (speed)
                `MIDDLE_SPEED: speed <= `HIGH_SPEED;
                `LOW_SPEED: speed <= `MIDDLE_SPEED;
            endcase
    end

endmodule
