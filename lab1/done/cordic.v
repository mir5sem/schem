// ?????? CORDIC ??? ?????????? ?????? ? ????????
module CORDIC(
    input clk,                // ??????? ????????? ???????? ??????
    input [31:0] angle,       // ???? ? ??????? 32-??????? ?????????????? ?????
    input [15:0] x_in, y_in,  // ??????? ???????? X ? Y
    output signed [16:0] sin_out, cos_out // ???????? ???????? ?????? ? ????????
);
 
// ??????? ????? ??????????? (atan) ??? ??????? ???? ????????
wire signed [31:0] atan_table [0:30];
`include "atan_table.vh" // ????????? ????? ? ???????? atan

// ???????? ??? ???????? ????????????? ???????? X, Y ? ?????
reg signed [31:0] X [31:0], Y [31:0], res_acc [31:0];

// ??????????? ???????? (?????????) ????
wire [1:0] quadrant = angle[31:30];

// ????????? ????????????? ???????? X, Y ? ????
always @(posedge clk) begin
    case (quadrant)
        2'b00, 2'b11: begin
            res_acc[0] <= angle;
            X[0] <= x_in; Y[0] <= y_in;
        end
        2'b01: begin
            res_acc[0] <= {2'b00, angle[29:0]};
            X[0] <= -y_in; Y[0] <= x_in;
        end
        2'b10: begin
            res_acc[0] <= {2'b11, angle[29:0]};
            X[0] <= y_in; Y[0] <= -x_in;
        end
    endcase
end

// ????????? ???????? ??? ?????????? ?????? ? ???????? ??????? CORDIC
genvar i;
generate
    for (i = 0; i < 31; i = i + 1) begin
        // ??????????? ????? ????????
        wire sign_rotation;
        assign sign_rotation = res_acc[i][31];
        
        // ?????? ???????? X ? Y ??? ??????? ????????
        wire signed [31:0] x_shift, y_shift;
        assign x_shift = X[i] >>> i;
        assign y_shift = Y[i] >>> i;

        // ?????????? ???????? X, Y ? ???? ?? ?????? ???????? ???????
        always @(posedge clk) begin
            X[i + 1] <= sign_rotation ? (X[i] + y_shift) : (X[i] - y_shift);
            Y[i + 1] <= sign_rotation ? (Y[i] - x_shift) : (Y[i] + x_shift);
            res_acc[i + 1] <= sign_rotation ? (res_acc[i] + atan_table[i]) : (res_acc[i] - atan_table[i]);
        end
    end
endgenerate

// ?????????? ???????? ?????? ? ???????? ?? ????????? ????????
assign sin_out = Y[31];
assign cos_out = X[31];

endmodule
