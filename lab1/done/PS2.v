`timescale 1ns / 1ps

// ???-?????? ??? ?????? ? PS/2 ???????????
module PS2(
    input clk,          // ????????? ???????? ??????
    input PS2_DATA,     // ????? ?????? PS/2
    input PS2_CLK,      // ????? ????????? ??????? PS/2
    output reg R_O,     // ???? ?????????? ?????????
    output [3:0] flags  // 4-?????? ???????? ???? ?????????
);

    // ??? ?????????? ??????? PS/2
    localparam [7:0] REALESE_CODE = 8'hF0;

    // ??????? ?? ?????? PS/2 Reader
    wire ERROR_READER, R_O_READER;  // ????? ?????? ? ?????????? ??????
    wire [7:0] code_reader;         // 8-?????? ??? ???????

    // ????????? ?????? ??? ?????? PS/2 ??????
    PS2_reader ps2_reader (
        .clk(clk),
        .PS2_DATA(PS2_DATA),
        .PS2_CLK(PS2_CLK),
        .ERROR(ERROR_READER),
        .R_O(R_O_READER),
        .code(code_reader)
    );

    // ????????? ?????? ??? ????????????? PS/2 ??????
    PS2_decoder ps2_decoder (
        .R_O_READER(R_O_READER),
        .code_reader(code_reader),
        .flags(flags)
    );

    // ???? ?????????? ???????
    reg realese_flag;
    reg state;

    // ????????? ???????? ??? ?????? ? ?????????
    initial begin
        state <= 0;
        R_O <= 0;
        realese_flag <= 0;
    end

    // ?????? ????????? ??? ????????? PS/2 ??????
    always @(posedge clk) begin
        case (state)
            // ????????? 0: ???????? ?????????? ?????? ??????
            1'b0: begin
                if (R_O_READER) begin
                    if (!ERROR_READER) begin
                        if (code_reader == REALESE_CODE) 
                            realese_flag <= 1;       // ????????? ????? ??????????
                        else if (realese_flag) begin
                            realese_flag <= 0;       // ????? ????? ??????????
                            R_O <= 1;               // ????????? ????? ?????????? ?????????
                        end
                    end
                    state <= 1;  // ??????? ? ????????? 1
                end
            end
            // ????????? 1: ????? ????? ?????????? ?????????
            1'b1: begin
                R_O <= 0;
                if (!R_O_READER)
                    state <= 0;  // ??????? ? ????????? 0
            end
        endcase
    end

endmodule
